	@RequestMapping(value = "/insertDailyAccount", method = RequestMethod.POST)
	@ResponseBody
	public String insertDailyAccount(@RequestBody JSONObject jsonObject){
		JsonData<String> jsonData = new JsonData<String>();
		try{
			String dailyAccount = jsonObject.getString("dailyAccount");
			String secret = jsonObject.getString("secret");
			String cId = jsonObject.getString("cId");
			
			boolean auth = false;
			if(StringUtils.isNotBlank(cId) && TryParseUtils.tryParse(cId, Long.class)){
				String mybaseSecret = companyService.getSecretById(Long.parseLong(cId));
				Map<String,Object> maps = new HashMap<String, Object>();
				maps.put("dailyAccount", dailyAccount);
				maps.put("secret", mybaseSecret);
				maps.put("cId", cId);
				String md5 = SHA1Utils.SHA1(maps);
				if(md5.equals(secret)){
					auth = true;
				}else{
					jsonData.setCode(ApiCode.AUTH_FAIL);
					jsonData.setMessage("验证失败");
				}
			}else{
				jsonData.setCode(ApiCode.ARGS_EXCEPTION);
				jsonData.setMessage("参数异常");
			}
			
			if(auth && StringUtils.isNotBlank(dailyAccount)){
				DailyAccountEntity dailyAccountObj = JSONObject.parseObject(dailyAccount, DailyAccountEntity.class);
				dailyAccountObj.setIsDelete(0);
				dailyAccountObj.setAddTime(new Date());
				dailyAccountObj.setId(UUID.randomUUID().toString().replace("-", ""));
				dailyAccountService.insertDailyAccount(dailyAccountObj);
				jsonData.setData(dailyAccountObj.getId().toString());
				jsonData.setCode(ApiCode.OK);
				jsonData.setMessage("操作成功");
			}else{
				jsonData.setCode(ApiCode.ARGS_EXCEPTION);
				jsonData.setMessage("参数异常");
			}
		}catch(Exception e){
			e.printStackTrace();
			jsonData.setCode(ApiCode.EXCEPTION);
			jsonData.setMessage("操作失败");
		}
		
		String result = JsonUtils.json2Str(jsonData);
		
		return result;
	}
	
	@RequestMapping(value = "/getDailyAccountPage", method = RequestMethod.POST)
	@ResponseBody
	public String getDailyAccountPage(@RequestBody JSONObject jsonObject){
		JsonData<List<DailyAccountEntity>> jsonData = new JsonData<List<DailyAccountEntity>>();
		try{
			String query = jsonObject.getString("query");
			String secret = jsonObject.getString("secret");
			String cId = jsonObject.getString("cId");
			
			boolean auth = false;
			if(StringUtils.isNotBlank(cId) && TryParseUtils.tryParse(cId, Long.class)){
				String mybaseSecret = companyService.getSecretById(Long.parseLong(cId));
				Map<String,Object> maps = new HashMap<String, Object>();
				maps.put("query", query);
				maps.put("secret", mybaseSecret);
				maps.put("cId", cId);
				String md5 = SHA1Utils.SHA1(maps);
				if(md5.equals(secret)){
					auth = true;
				}else{
					jsonData.setCode(ApiCode.AUTH_FAIL);
					jsonData.setMessage("验证失败");
				}
			}else{
				jsonData.setCode(ApiCode.ARGS_EXCEPTION);
				jsonData.setMessage("参数异常");
			}
			
			if(auth){
				DailyAccountQuery dailyAccountQuery = JSONObject.parseObject(query, DailyAccountQuery.class);

				Integer page =  dailyAccountQuery.getPage();
				if(page == null){
					dailyAccountQuery.setPage(1);
				}
				
				Integer size =  dailyAccountQuery.getSize();
				if(size == null){
					dailyAccountQuery.setSize(10);
				}
				
				dailyAccountQuery = dailyAccountService.getDailyAccountPage(dailyAccountQuery);
				jsonData.setCode(ApiCode.OK);
				jsonData.setMessage("操作成功");
				jsonData.setData(dailyAccountQuery.getItems());
				jsonData.setCount(dailyAccountQuery.getCount());
			}else{
				jsonData.setCode(ApiCode.ARGS_EXCEPTION);
				jsonData.setMessage("参数异常");
			}
		}catch(Exception e){
			e.printStackTrace();
			jsonData.setCode(ApiCode.EXCEPTION);
			jsonData.setMessage("操作失败");
		}
		
		String result = JsonUtils.json2Str(jsonData);
		
		return result;
	}
	
